package de.qaware.rat.api.enums;

/**
 * The {@code RuleEngine} ENUM holds fields to choose the rule engine for the application.
 * 
 * <p>
 * The ALL field will run all possible engines, i.e. include the JAVA and RUTA option.
 * 
 * @author Matthias
 *
 */
public enum RuleEngine {
    JAVA, RUTA, ALL;
}